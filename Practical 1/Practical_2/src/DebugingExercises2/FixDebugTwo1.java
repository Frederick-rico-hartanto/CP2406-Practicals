package DebugingExercises2;

public class FixDebugTwo1 {
    public static void main(String[] args)
    {
        int oneInt = 315; //Int should have no decimal
        double oneDouble = 12.4;
        char oneChar = 'A'; //Missing quotation
        System.out.print("The int is ");
        System.out.println(oneInt); //Typo
        System.out.print("The double is ");
        System.out.println(oneDouble);
        System.out.print("The char is "); //Missing quotation
        System.out.println(oneChar);
    }

}


